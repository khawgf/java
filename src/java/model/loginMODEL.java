package model;

/**
 *
 * @author DELL
 */
public class loginMODEL {
     private String id;
    private String phone;
    private String name;

    public loginMODEL() {
    }

    public loginMODEL(String id, String phone, String name) {
        this.id = id;
        this.phone = phone;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
